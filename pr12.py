# Завдання 1: Швидкий пошук
міста = ["Київ", "Одеса", "Львів", "Харків", "Житомир"]
множина_міст = set(міста)

for місто in ["Одеса", "Полтава"]:
    if місто in множина_міст:
        print(f"{місто} є у списку.")
    else:
        print(f"{місто} немає у списку.")

# Завдання 2: Пошук за словником
студенти = {"Іван": 80, "Марія": 95, "Олег": 78, "Анна": 85}
name = input("\nВведіть ім'я студента: ")

try:
    print(f"Оцінка студента {name}: {студенти[name]}")
except KeyError:
    print("Студента не знайдено.")

# Завдання 3: Оптимізація повторів
import random

числа = [random.randint(1, 1000) for _ in range(10000)]
повтори = {}

for число in числа:
    if число in повтори:
        повтори[число] += 1
    else:
        повтори[число] = 1

макс_число = max(повтори, key=повтори.get)
print(f"\nНайчастіше число: {макс_число} (повторень: {повтори[макс_число]})")
